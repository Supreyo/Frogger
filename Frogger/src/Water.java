public class Water{
	private int x, y, w, h; //All parameters for the Water class
	
	Water(int x, int y, int w, int h){
		this.x = x;
		this.y = y;
		this.w = w;
		this.h = h;
	}
	
	public int getWaterX() { //Returning all getter methods
		return x;
	}
	
	public int getWaterY() {
		return y;
	}

	public int getWaterW() {
		return w;
	}
	
	public int getWaterH() {
		return h;
	}	
}

