public class Hearts{
	private int x, y, w, h; //All parameters for the Hearts class
	
	Hearts(int x, int y, int w, int h){
		this.x = x;
		this.y = y;
		this.w = w;
		this.h = h;
	}
	
	public int getHeartX() { //Returning all getter methods
		return x;
	}
	
	public int getHeartY() {
		return y;
	}

	public int getHeartW() {
		return w;
	}
	
	public int getHeartH() {
		return h;
	}	
}